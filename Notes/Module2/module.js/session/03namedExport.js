// This function calculates the product of two numbers (a and b)
// It logs the result in a formatted string to the console
function multiply(a, b) {
  console.log(`The product of two numbers is ${a * b}`);
}

// This function calculates the quotient of two numbers (x and y)
// It logs the result in a formatted string to the console
function divide(x, y) {
  console.log(`The quotient of two numbers is ${x / y}`);
}

